# Sets : Values that are unordered,No duplicates.
languages = {'Python','Java','C++','Kotlin'}
print(languages)
languages.add('R')
print(languages)
languages.copy()
print(languages)
languages_android = {'Java','Kotlin'}
print(languages.difference(languages_android))
print(languages.intersection(languages_android))
# we can also use Union(),Intersection(),etc use cases.